    public void filltable(ArrayList<ModelHoaDon> list) {
        mol = (DefaultTableModel) tbl_HoaDon.getModel();
        mol.setRowCount(0);
        int i = 1;
        for (ModelHoaDon x : list) {
            String trangThai = x.isTrangThai() == true ? "Đã thanh toán" : "Chưa thanh toán";
            mol.addRow(new Object[]{i++, x.getMaHoaDon(), x.getTenKhachHang(), x.getSdt(), x.getTenNhanVien(), x.getTenKm(), x.getNgayTao(), x.getTongTien(), x.getTienGiamGia(),x.getTienKhachTra(), trangThai}
            );
        }
    }

    public void filltableHDCT(ArrayList<ModelHDCT> list) {
        mol = (DefaultTableModel) tbl_HDCT.getModel();
        mol.setRowCount(0);
        int i = 1;
        for (ModelHDCT x : list) {
            mol.addRow(new Object[]{i++, x.getMaHDCT(), x.getMaSanPham(), x.getTenSanPham(), x.getSoLuong(), x.getDonGia(), x.getTenChatLieu(), x.getTenMauSac(), x.getTenKichThuoc(), x.getXuatXu()}
            );
        }
    }

